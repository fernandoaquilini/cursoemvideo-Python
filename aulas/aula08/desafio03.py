from math import sin, cos, tan
angulo = float(input('Entre com o Valor do Ângulo X: '))
print('O Valor do Seno do Ângulo {}º é {}.'.format(angulo, sin(angulo)))
print('O Valor do Cosseno do Ângulo {}º é {}.'.format(angulo, cos(angulo)))
print('O Valor da Tangente do Ângulo {}º é {}.'.format(angulo, tan(angulo)))
