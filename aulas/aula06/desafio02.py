valor = input('Digite qualquer coisa: ')
print('O tipo primitivo é {}'.format(type(valor)))

print('A variável é formado somente por espaços? {}'.format(valor.isspace()))
print('A variável é maiúscula? {}'.format(valor.isupper()))
print('A variável é minúscula? {}'.format(valor.islower()))
print('A variável está capitalizada (maiúsculas e minúsculas)? {}'.format(valor.istitle()))
print('A variável é um número inteiro? {}'.format(valor.isnumeric()))
print('A variável é um número decimal? {}'.format(valor.isdecimal()))
print('A variável é formada apenas por números? {}'.format(valor.isdigit()))
print('A variável é alfabética? {}'.format(valor.isalpha()))
print('A variável é alfanumérica? {}'.format(valor.isalnum()))
print('A variável é ASCII? {}'.format(valor.isascii()))
print('A variável é printable? {}'.format(valor.isprintable()))
print('A variável possui sintaxe para identificadores? {}'.format(valor.isidentifier()))





